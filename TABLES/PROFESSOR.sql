CREATE TABLE PROFESSOR (
  -> SSN numeric(9) PRIMARY KEY,
  -> DNO numeric(5),
  -> Name varchar(30),
  -> SAddress varchar(100),
  -> City varchar(20),
  -> State varchar(20),
  -> Zipcode numeric(5),
  -> Areacode numeric(3),
  -> Digits numeric(7),
  -> Sex varchar(6),
  -> Title varchar(30),
  -> Salary numeric(10),
);
